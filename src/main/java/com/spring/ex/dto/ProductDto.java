package com.spring.ex.dto;

public class ProductDto {
	// 상품명
	private String pName;
	// 상품 카테고리
	private String pCate;
	// 상품 가격
	private String pPrice;
	// 상품 배송
	private String pShip;
	// 상품 수량
	private String pCount;
	// 상품판매수량
	private String pCountSell;
	// 상품 누적판매액
	private String pCumulSell;
	// 상품 적립 마일리ㅣ
	private String pMile;
	// 상품이미지 URL
	private String pUrl;
	// 상품구매자 수(남성)
	private String pMan;
	// 상품구매자 수(여성)
	private String pWoman;
	
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getpCate() {
		return pCate;
	}
	public void setpCate(String pCate) {
		this.pCate = pCate;
	}
	public String getpPrice() {
		return pPrice;
	}
	public void setpPrice(String pPrice) {
		this.pPrice = pPrice;
	}
	public String getpShip() {
		return pShip;
	}
	public void setpShip(String pShip) {
		this.pShip = pShip;
	}
	public String getpCount() {
		return pCount;
	}
	public void setpCount(String pCount) {
		this.pCount = pCount;
	}
	public String getpCountSell() {
		return pCountSell;
	}
	public void setpCountSell(String pCountSell) {
		this.pCountSell = pCountSell;
	}
	public String getpCumulSell() {
		return pCumulSell;
	}
	public void setpCumulSell(String pCumulSell) {
		this.pCumulSell = pCumulSell;
	}
	public String getpMile() {
		return pMile;
	}
	public void setpMile(String pMile) {
		this.pMile = pMile;
	}
	public String getpUrl() {
		return pUrl;
	}
	public void setpUrl(String pUrl) {
		this.pUrl = pUrl;
	}
	public String getpMan() {
		return pMan;
	}
	public void setpMan(String pMan) {
		this.pMan = pMan;
	}
	public String getpWoman() {
		return pWoman;
	}
	public void setpWoman(String pWoman) {
		this.pWoman = pWoman;
	}
	
	
}
